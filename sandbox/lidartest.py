newmap = OccupancyGrid(np.zeros((72,72)),4)
xt_newmap = np.array([120,180, 0, 0, 0, 0])

def updateNewMap(new_oc, xt, ls):
    newmap = new_oc._oc
    for i in range(len(ls)):
        for j in range(len(ls[i])):
            xls = ls[i][j][0] + int(xt[0]/4)
            yls = ls[i][j][1] + int(xt[1]/4)
            if(j == len(ls[i]) - 1):
                # newmap[yls][xls] = min(newmap[yls][xls] + 20, 50)
                newmap[yls][xls] = 0
            else:
                # newmap[yls][xls] -= max(newmap[yls][xls] - 20, -50)
                newmap[yls][xls] = 1
    new_oc._oc = newmap

